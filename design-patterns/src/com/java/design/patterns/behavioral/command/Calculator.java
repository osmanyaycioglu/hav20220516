package com.java.design.patterns.behavioral.command;


public class Calculator {

    public double add(final double val1,
                      final double val2) {
        return val1 + val2;
    }

    public double subs(final double val1,
                       final double val2) {
        return val1 + val2;
    }

    public double multiply(final double val1,
                           final double val2) {
        return val1 + val2;
    }

}
